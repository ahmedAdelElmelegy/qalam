  /// Prepares Arabic text for TTS by normalizing and applying Tashkeel fixes.
  static String prepareForTts(String text) {
    if (text.trim().isEmpty) return text;

    String result = text.trim();

    // 1. Isolated letter/form → spoken description
    if (_characterFixes.containsKey(result)) {
      return _characterFixes[result]!;
    }

    // 2. Isolated letter with Haraka diacritic
    if (result.length == 2 && _isHaraka(result[1])) {
      if (result[1] == '\u0651' || result[1] == '\u0652') {
        return 'أَ$result';
      }
      return '$resultـ';
    }

    // 3. Isolated Maddah 'آ'
    if (result == 'آ') return 'آم';

    // 4. Word-level Tashkeel fixes (whole-word boundary match)
    _tashkeelFixes.forEach((key, value) {
      final regex = RegExp('(?<=^|\\s)$key(?=\\s|\$|\\.|،|؟|!)');
      result = result.replaceAll(regex, value);
    });

    return result;
  }

  static bool _isHaraka(String char) {
    const harakat = '\u064B\u064C\u064D\u064E\u064F\u0650\u0651\u0652';
    return harakat.contains(char);
  }
